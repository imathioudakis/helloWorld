package com.company;

public class ConditionalStatements {
    public static void main(String[] args) {
        int x = 0;
        if (x > 0) {
            System.out.println("The value of x is positive");
        }
        else if ( x < 0) {
            System.out.println("The value is negative");
        }
        else {
            System.out.println("The value is zero");
        }
    }

}
